const LABELS: Record<PassVariant, { band: string; code: string }> = {
  tier0: { band: "TIER 0", code: "T0" },
  pro: { band: "PRO CLEARANCE", code: "PRO" },
  annual: { band: "PRO ANNUAL", code: "12M" },
  day: { band: "DAY PASS", code: "24H" },
};

interface Props {
  variant: PassVariant;
  className?: string;
  featured?: boolean;
}

export function ClearancePass({ variant, className = "", featured = false }: Props) {
  const art = ARTS[variant];
  const palette = PALETTES[variant];
  const label = LABELS[variant];
  const w = art[0]?.length ?? 28;
  const h = art.length;

  return (
    <div
      className={`relative ${featured ? "w-full max-w-[220px]" : "w-full max-w-[168px]"} ${className}`}
    >
      <div
        className="pointer-events-none absolute -top-3 left-1/2 z-10 h-8 w-1.5 -translate-x-1/2 rounded-sm bg-wire-700"
        aria-hidden
      />
      <div
        className={`clearance-pass-frame ${featured ? "clearance-pass-frame--featured" : ""}`}
      >
        <svg
          viewBox={`0 0 ${w} ${h}`}
          className="clearance-pass-art w-full"
          shapeRendering="crispEdges"
          aria-hidden
        >
          {art.map((row, y) =>
            row.map((cell, x) =>
              cell ? (
                <rect
                  key={`${x}-${y}`}
                  x={x}
                  y={y}
                  width={1}
                  height={1}
                  fill={palette[cell]}
                />
              ) : null,
            ),
          )}
        </svg>
        <div className="clearance-pass-label">
          <span className="font-mono text-[8px] font-semibold uppercase tracking-[0.28em] text-wire-300">
            {label.band}
          </span>
          <span className="font-mono text-[11px] font-bold tracking-[0.2em] text-wire-100 brass-glow-soft">
            {label.code}
          </span>
        </div>
      </div>
    </div>
  );
}
